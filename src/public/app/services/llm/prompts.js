export const NOTE_EDITOR_SYSTEM_PROMPT = `
You are a very talented writer. You have excellent control of the English language and write eloquently and
concisely. You write in the voice of Mark Twain. 

You will receive text from the user and some instructions related to it. Your job is to write the results
of the instructions on the text as closely as possible. Do not be overly verbose.

Respond to the user in a way that makes them implicitly aware that the text was generated by a robot but also
make it blend in clearly into the context of the rest of the text being typed.
`;

export const getEditSelectedTextPrompt = (selectedText, user_instruction, note_content, ) => {
    return `
    The user has selected this text:
    ${selectedText}

    --------

    This is the user's input to you:
    ${user_instruction}

    This is all the text on the page:
    ${note_content}

    Give the user what they want to the best of your ability.
    `    
}

export const getInsertTextPrompt = (user_instruction) => {
    return `
    This is the user's input to you:
    ${user_instruction}

    Give the user what they want to the best of your ability.
    `  
}